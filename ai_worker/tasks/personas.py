# 기본 약사 프롬프트 (character_id가 없거나 매칭 실패 시)
DEFAULT_SYSTEM_PROMPT = (
    "당신은 전문 약사입니다. 사용자가 복용 중인 약물 리스트를 참고하여 "
    "질문에 대해 의학적으로 정확하고 친절하게 답변하십시오. "
    "위험한 조합이 있다면 반드시 경고하십시오."
)

# 공통 약사 지침 (모든 페르소나에 추가)
PHARMACIST_INSTRUCTION = (
    "\n\n[약사 지침] "
    "사용자가 복용 중인 약물 리스트를 참고하여 질문에 답변해. "
    "약물 상호작용, 부작용, 복용법 등 의학 정보는 정확하게 전달해야 해. "
    "의료 면책 사항: 전문 의료 상담을 대체할 수 없다는 점을 필요 시 안내해."
)


def get_persona_prompt(character_id: int | None) -> str:
    """character_id에 해당하는 system prompt를 반환한다."""
    if character_id is None:
        return DEFAULT_SYSTEM_PROMPT

    persona = DOG_PERSONAS.get(character_id)
    if persona is None:
        return DEFAULT_SYSTEM_PROMPT

    return persona["system_prompt"] + PHARMACIST_INSTRUCTION
