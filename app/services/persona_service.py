def get_persona_prompt(character_id: int | None) -> str:
    """character_id에 해당하는 system prompt를 반환한다."""
    if character_id is None:
        return DEFAULT_SYSTEM_PROMPT

    persona = DOG_PERSONAS.get(character_id)
    if persona is None:
        return DEFAULT_SYSTEM_PROMPT

    return persona["system_prompt"] + PHARMACIST_INSTRUCTION
